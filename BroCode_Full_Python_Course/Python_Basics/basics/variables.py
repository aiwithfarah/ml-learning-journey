# Strings - sequence of characters enclosed in quotes
first_name = "Haraf"
food = "Indian food"
email = "Haraf@bln.com"

print(f"Hi {first_name}") #Hi Haraf
print(f"You like {food}") #You like Indian food
print(f"Your email is {email}") #Your email is Haraf@bln.com

# Integers : +ve and -Ve numbers w/o decimal points
age = 34
print(f"You are {age} years old!") #You are 34 years old!

#Floating Point Numbers : +ve and -ve nums with a decimal part
weight = 53.5
print(f"{first_name} weighs {weight}kgs") #Haraf weighs 53.5kgs

#Boolean is either True or False
is_learning_python = True
print(f"Is {first_name} learning Python? : {is_learning_python}")
#Is Haraf learning Python? : True

